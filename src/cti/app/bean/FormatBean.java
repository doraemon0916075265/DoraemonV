package cti.app.bean;

public class FormatBean {
	private String specFilePath;
	private String formatFilePath;
	private String specID_Format;
	private String format;
	private String input;
	private String output;
	private String exportFilePath;

	public String getSpecFilePath() {
		return specFilePath;
	}

	public void setSpecFilePath(String specFilePath) {
		this.specFilePath = specFilePath;
	}

	public String getFormatFilePath() {
		return formatFilePath;
	}

	public void setFormatFilePath(String formatFilePath) {
		this.formatFilePath = formatFilePath;
	}

	public String getSpecID_Format() {
		return specID_Format;
	}

	public void setSpecID_Format(String specID_Format) {
		this.specID_Format = specID_Format;
	}

	public String getFormat() {
		return format;
	}

	public void setFormat(String format) {
		this.format = format;
	}

	public String getInput() {
		return input;
	}

	public void setInput(String input) {
		this.input = input;
	}

	public String getOutput() {
		return output;
	}

	public void setOutput(String output) {
		this.output = output;
	}

	public String getExportFilePath() {
		return exportFilePath;
	}

	public void setExportFilePath(String exportFilePath) {
		this.exportFilePath = exportFilePath;
	}

}
